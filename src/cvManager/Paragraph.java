package cvManager;

public class Paragraph {
	private String contents;

	public Paragraph(String contents) {
		super();
		this.contents = contents;
	}

	public Paragraph() {
		super();
	}

	public String getContents() {
		return contents;
	}

	public void setContents(String contents) {
		this.contents = contents;
	}
	
}
